int GameClearScene_Initialize(void)
{
	int ret = 0;

	//画面の読み込み
	GameClearImage = LoadGraph("images/gameclear.png");

	//音源の読み込み
	GameClearSE = LoadSoundMem("sounds/gamwclear_se.mp3");

	GameClearFlag = 0;

	//エラーチェック
	if (GameClearImage == -1)
	{
		ret = -1;
	}
	if (GameClearSE == -1)
	{
		ret = -1;
	}
	return ret;
    
}

